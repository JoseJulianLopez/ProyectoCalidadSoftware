    public void editarFactura(Factura facturaActual) {
        this.setTitle("Ver Factura");
        btnAgregar.setVisible(false);

        txtNoFactura.setText(String.valueOf(facturaActual.getNumeroFactura()));
        txtNoFactura.setEnabled(false);
        txtFecha.setText(facturaActual.getFecha());
        txtFecha.setEnabled(false);
        cbCliente.setSelectedItem(facturaActual.getNicknameCliente());
        cbCliente.setEnabled(false);
        cbEquipo.setSelectedItem(facturaActual.getNombreEquipo());
        cbEquipo.setEnabled(false);
        cbEmpleado.setSelectedItem(facturaActual.getNicknameEmpleado());
        cbEmpleado.setEnabled(false);
        txtDetalleReparcion.setText(facturaActual.getDetalleReparacion());
        txtDetalleReparcion.setEnabled(false);
        txtTotalPagar.setText(String.valueOf(facturaActual.getMontoAPagar()));
        txtTotalPagar.setEnabled(false);
        checkEstado.setSelected(facturaActual.isEstado());
        checkEstado.setEnabled(false);

    }

