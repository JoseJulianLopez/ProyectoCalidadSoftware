    public void cargarEmpleadosActivos() {
        cbEmpleado.removeAllItems();
        for (int i = 0; i < Globales.Empleados.size(); i++) {
            if (Globales.Empleados.get(i).isEstado() == true) {
                cbEmpleado.addItem(Globales.Empleados.get(i).getNickname()); // esto inserta en el combobox
            }

        }
    }

    public void cargarClientesActivos() {
        cbCliente.removeAllItems();
        for (int i = 0; i < Globales.Clientes.size(); i++) {
            if (Globales.Clientes.get(i).isEstado() == true) {
                cbCliente.addItem(Globales.Clientes.get(i).getNickname()); // esto inserta en el combobox
            }

        }
    }

    public void cargarEquiposActivos() {
        cbEquipo.removeAllItems();
        for (int i = 0; i < Globales.Equipos.size(); i++) {
            if (Globales.Equipos.get(i).getNicknameCliente().equalsIgnoreCase(cbCliente.getSelectedItem().toString())) {
                if (Globales.Equipos.get(i).isEstado() == true) {
                    cbEquipo.addItem(Globales.Equipos.get(i).getNombreEquipo()); // esto inserta en el combobox
                }
            }

        }
    }

    public void calcularNumeroFactura() { // esto sirve para obtener la ultima factura
        int max = 0;
        for (int i = 0; i < Globales.Facturas.size(); i++) {
            if (Globales.Facturas.get(i).getNumeroFactura() > max) {
                max = Globales.Facturas.get(i).getNumeroFactura();
            }
        }
        txtNoFactura.setText(String.valueOf(max + 1));
    }

    public void calcularFechaActual() { // esto sirve para obtener el dia
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        txtFecha.setText(dateFormat.format(date));
        txtFecha.setEnabled(false);
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Validamos que la sucursal escribrio todos los datos\
        Factura f = new Factura();

        boolean encontrado = false;

        try {
            if (txtFecha.getText().equalsIgnoreCase("")) {
                throw new Exception("Debe escribir la fecha del dia de hoy");
            }
            if (cbCliente.getSelectedIndex() < 0) {
                throw new Exception("Debe seleccionar el cliente para la factura");
            }
            if (cbEquipo.getSelectedIndex() < 0) {
                throw new Exception("Debe seleccionar un equipo para la factura");
            }
            if (cbEmpleado.getSelectedIndex() < 0) {
                throw new Exception("Debe seleccionar un empleado para la factura");
            }
            if (txtDetalleReparcion.getText().equalsIgnoreCase("")) {
                throw new Exception("Debe escribir un detalle para la factura");
            }
            if (txtTotalPagar.getText().equalsIgnoreCase("")) {
                throw new Exception("Debe escribir el total de la repacion");
            }
            if (isDouble(txtTotalPagar.getText()) == false) {
                throw new Exception("El valor total a pagar no es valido");

            }

            f.setNumeroFactura(Integer.parseInt(txtNoFactura.getText()));
            f.setFecha(txtFecha.getText());
            f.setNicknameCliente(cbCliente.getSelectedItem().toString());
            f.setNombreEquipo(cbEquipo.getSelectedItem().toString());
            f.setNicknameEmpleado(cbEmpleado.getSelectedItem().toString());
            f.setDetalleReparacion(txtDetalleReparcion.getText());
            f.setMontoAPagar(Double.parseDouble(txtTotalPagar.getText()));
            f.setEstado(checkEstado.isSelected());

            // de aqui para abajo es el metodo para agregar una factura
            if (btnAgregar.getText().equalsIgnoreCase("Agregar")) {

                // Este for verifica si ya existe el codigo en la lista de categorias
                for (int i = 0; i < Globales.Facturas.size(); i++) {
                    if (Globales.Facturas.get(i).getNumeroFactura() == f.getNumeroFactura()) {
                        encontrado = true;
                    }
                }
                if (encontrado == true) {
                    throw new Exception("Ya existe una factura con ese numero");
                } else {
                    Globales.Facturas.add(f);
                    JOptionPane.showMessageDialog(null, "La Factura: " + f.getNumeroFactura() + " ha sido agregada exitosamente");
                    this.setVisible(false);
                    new ListadoFacturas().setVisible(true);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al agregar los datos: " + e.getMessage(), "Error!",
                    JOptionPane.INFORMATION_MESSAGE);
        }

        Globales.guardarFacturas();


    }//GEN-LAST:event_btnAgregarActionPerformed

