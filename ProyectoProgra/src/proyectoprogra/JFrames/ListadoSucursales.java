    public void cargarListaSucursales() {
        DefaultTableModel model = (DefaultTableModel) tablaSucursal.getModel();
        Object[] dataRow;
        model.setRowCount(0);
        for (int i = 0; i < Globales.Sucursales.size(); i++) {
            dataRow = new Object[]{
                Globales.Sucursales.get(i).getNombre(),
                Globales.Sucursales.get(i).getCiudad(),
                Globales.Sucursales.get(i).getDireccion(),
                Globales.Sucursales.get(i).getTelefono(),
                Globales.Sucursales.get(i).getCorreo(),
                Globales.Sucursales.get(i).isEstado()
            };
            model.addRow(dataRow);

        }

    }


