    private void btnEditar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditar
        int row = tablaSucursal.getSelectedRow();
        AgregarSucursal agru = new AgregarSucursal();
        if (row >= 0) {
            String nombre = tablaSucursal.getModel().getValueAt(row, 0).toString();
            for (int i = 0; i < Globales.Sucursales.size(); i++) {
                if (Globales.Sucursales.get(i).getNombre().equalsIgnoreCase(nombre)) {
                    agru.editarSucursal(Globales.Sucursales.get(i));
                    agru.setVisible(true);
                    this.setVisible(false);
                    break;
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una sucursal");
        }
    }//GEN-LAST:event_btnEditar

